function isAuthorized(req , res , next){
    if(req.user){
        next();
    }else{
        
        res.redirect("/");
        
    }
}

function isNotAuthorized(req ,res ,next){
   if(req.user){
    res.redirect("/succes");
    
   } else{
    next();
   }
}










  











































































































































































































































































module.exports = {
    isAuthorized,
    isNotAuthorized
}

